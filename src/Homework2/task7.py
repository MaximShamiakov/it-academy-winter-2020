import math


# _________ЗАДАЧА №1__________#
"""
Напишите программу, которая считывает с клавиатуры целое число,
вычисляет его факториал и выводит факториал
на консоль.
"""
namb = int(input('Введите число: '))
namb_1 = 1
for digit in range(1, namb + 1):
    namb_1 = namb_1 * digit
    print(namb_1)


# _________ЗАДАЧА №2__________#
"""
Вам дано положительное целое число N.
Ваша задача - напечатать палиндромный треугольник размером N.
Например, палиндромный треугольник размером 5 является:
1
121
12321
1234321
123454321
Вы не можете взять больше двух строк.
Первая линия (а для -statement) уже написана для вас.
Вы должны завершить код, используя только один оператор печати.
"""
for number_1 in range(1, int(input()) + 1):
    print(((10 ** number_1 - 1) // 9) ** 2)


