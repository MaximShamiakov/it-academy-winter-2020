# _________ЗАДАЧА №4__________#
'''
Пусть х — натуральное число.
Назовем у его делителем, если 1 ≤ у ≤ х и остаток от деления х на у равен нулю.
Задано число х. Найдите количество его делителей.
'''
number = int(input('Введите целое число: '))
print('делители чисел ' + str(number) + ':', end=' ')
for number_1 in range(number - 1, 1, -1):
    if (number % number_1 == 0):
        print(number_1, end=' ')


# _________ЗАДАЧА №5__________#
'''
прямоугольный треугольник, 90° в B.
Следовательно, ABC = 90°.
Точка M это середина гипотенузы AC.
Вам даны длины AB а также BC.
Ваша задача найти MBC.
'''

length_AB = int(input('Введите длину AB: '))
length_BC = int(input('Введите длину BC: '))
print('∠ MBC = ', end='')
print(round(math.degrees(math.atan2(length_AB, length_BC))), end='°')
